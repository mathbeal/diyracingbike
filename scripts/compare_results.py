#!/usr/bin/env python3
"""
compare_results.py — Generates a Markdown comment comparing two race runs.

Usage:
    python scripts/compare_results.py baseline.json pr.json
    → prints the Markdown comment to stdout
"""

import json
import sys


def generate_comment(baseline: dict, pr: dict) -> str:
    """Generates the Markdown comparison comment."""
    lines = []
    lines.append("## 🚴 Race Results — Comparison")
    lines.append("")

    # Main table
    lines.append("| | Baseline | This PR |")
    lines.append("|---|---|---|")
    lines.append(f"| **Winner** | Team {baseline['winner']} | Team {pr['winner']} |")
    lines.append(f"| **Ticks** | {baseline['ticks']} | {pr['ticks']} |")

    b_order = ", ".join(baseline["finished_order"][:5]) + ("..." if len(baseline["finished_order"]) > 5 else "")
    p_order = ", ".join(pr["finished_order"][:5]) + ("..." if len(pr["finished_order"]) > 5 else "")
    lines.append(f"| **Finishing Order (top 5)** | {b_order} | {p_order} |")
    lines.append("")

    # Diff of initial conditions
    b_energies = baseline["config_summary"]["initial_energies"]
    p_energies = pr["config_summary"]["initial_energies"]

    changed = {
        cid: (b_energies.get(cid, "?"), p_energies.get(cid, "?"))
        for cid in set(b_energies) | set(p_energies)
        if b_energies.get(cid) != p_energies.get(cid)
    }

    if changed:
        lines.append("### Modified Initial Conditions")
        lines.append("")
        lines.append("| Cyclist | Baseline | PR | Δ |")
        lines.append("|---|---|---|---|")
        for cid in sorted(changed):
            b_e, p_e = changed[cid]
            delta = p_e - b_e if isinstance(p_e, int) and isinstance(b_e, int) else "?"
            sign = "+" if isinstance(delta, int) and delta > 0 else ""
            lines.append(f"| {cid} | {b_e} | {p_e} | {sign}{delta} |")
        lines.append("")

        # Automatic analysis
        tick_diff = pr["ticks"] - baseline["ticks"]
        faster = "faster" if tick_diff < 0 else "slower" if tick_diff > 0 else "identical in duration"
        winner_changed = baseline["winner"] != pr["winner"]
        lines.append("### Impact Analysis")
        lines.append("")
        if winner_changed:
            lines.append(
                f"The change in initial conditions **changed the winner** "
                f"(Team {baseline['winner']} → Team {pr['winner']})."
            )
        else:
            lines.append(f"The winner remains Team {pr['winner']}.")
        lines.append(
            f"The race is {faster} ({abs(tick_diff)} tick{'s' if abs(tick_diff) != 1 else ''} "
            f"{'fewer' if tick_diff < 0 else 'more' if tick_diff > 0 else ''}).".strip()
        )
    else:
        lines.append("*No changes to initial conditions — results may differ due to LLM agent variability.*")

    lines.append("")
    lines.append("---")
    lines.append("*Automatically generated by `scripts/compare_results.py`*")

    return "\n".join(lines)


def main():
    if len(sys.argv) != 3:
        print("Usage: python scripts/compare_results.py baseline.json pr.json", file=sys.stderr)
        sys.exit(1)

    with open(sys.argv[1]) as f:
        baseline = json.load(f)
    with open(sys.argv[2]) as f:
        pr = json.load(f)

    print(generate_comment(baseline, pr))


if __name__ == "__main__":
    main()
